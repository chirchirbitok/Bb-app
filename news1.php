			<p>Former NBA player Jerome Williams, who played for teams like the Detroit Pistons, Toronto Raptors, Chicago Bulls and New York Knicks during his nine-year tenure, is in Nigeria this week for the finals of Power Forward.</p>
			<p>The close of the fourth season of the NBA’s development program will be hosted tomorrow, Thursday, 28 September at the National Stadium in Abuja.</p>
			<p>Power Forward is operated by the NBA in partnership with ExxonMobil and international NGO Africare, aimed at fostering youth development through sports, life skills and raising public-health awareness among high-school youth.</p>
			<p>In addition to basketball, Power Forward teaches health literacy and life skills such as leadership, respect and personal responsibility to positively impact Nigerian secondary-school students.</p>
			<p>This year’s finals event will feature boys and girls championship games, a malaria testing station, public-health awareness activations as well as performances from an NBA Dunk Squad.</p>
			<p>The Power Forward program, launched in November 2013, directly impacts annually 300 students from 10 secondary schools in Abuja.</p>
			<p>As part of its corporate social responsibility initiatives, ExxonMobil uses the program to supplement its efforts in contributing to the fight against the global burden of malaria. Over the past 17 years, ExxonMobil-supported programs have distributed more than 14-million bed nets, about 3-million diagnostic tests and more than 4-million antimalarial treatments – reaching more than 125-million people worldwide</p>
			<p>From a basketball perspective, last year eight boys represented the program at the first annual Jr. NBA Africa Festival in South Africa and Marvelous Nwoko from Government Secondary School Zone 3, represented the Power Forward Program at the 15th Basketball Without Borders Africa girls camp in South Africa.</p>
			<p>Since its inception, the program’s season launch and finals events have been attended by NBA and WNBA players and legends including Hall of Famer Hakeem Olajuwon, former NBA players Obinna Ekezie, Olumide Oyedeji and Kelenna Azubuike, WNBA All-Stars Swin Cash and Chiney Ogwumike and former WNBA player Mactabene Amachree.</p>
